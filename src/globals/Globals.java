package globals;

import java.awt.event.KeyEvent;

public abstract class Globals {
	
	public abstract static class KeyCodes {
		public static int FIRE_LASER = KeyEvent.VK_SPACE;
		public static int UP = KeyEvent.VK_UP;
		public static int DOWN = KeyEvent.VK_DOWN;
		public static int LEFT = KeyEvent.VK_LEFT;
		public static int RIGHT = KeyEvent.VK_RIGHT;
		public static int SCAN = KeyEvent.VK_S;
		public static int NEXT_TARGET = KeyEvent.VK_R;
		public static int PREV_TARGET = KeyEvent.VK_E;
		
	}

}
